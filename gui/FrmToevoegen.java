    private void vulTblSamenkoms() {
       
        String[] kols = {"refnum", "naam1", "naam2", "straat", "postcode",
            "woonplaats", "dag", "uur"};

        DefaultTableModel mdlTable = new DefaultTableModel(kols, 0);

        tblGegevens.setModel(mdlTable);

        for(int i = 0; i < samenkomsten.size(); i++) {
            int refnum = samenkomsten.get(i).getRefnum().getRefnum();
            String naam1 = samenkomsten.get(i).getNaam1();
            String naam2 = samenkomsten.get(i).getNaam2();
            String straat = samenkomsten.get(i).getStraat();
            String postcode = samenkomsten.get(i).getPostcode();
            String woonpl = samenkomsten.get(i).getWoonpl();
            String dag = samenkomsten.get(i).getDag();
            String uren = "";
            String minuten = "";
            if (samenkomsten.get(i).getUur().get(GregorianCalendar.HOUR_OF_DAY) < 10) {
                uren = "0";
            }
            if (samenkomsten.get(i).getUur().get(GregorianCalendar.MINUTE) < 10) {
                minuten = "0";
            }
            String uur = uren + samenkomsten.get(i).getUur().get(GregorianCalendar.HOUR_OF_DAY) + ":" + minuten + samenkomsten.get(i).getUur().get(GregorianCalendar.MINUTE);
            Object[] data = {refnum, naam1, naam2, straat, postcode, woonpl, dag, uur};
            mdlTable.addRow(data);
            
        }
       
        
    }
    
