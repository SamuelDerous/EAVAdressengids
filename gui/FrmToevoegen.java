            samenkomst.setTelefoon(txtSmTelefoon.getText());
            samenkomst.setDag(cmbSmDag.getSelectedItem().toString());
            GregorianCalendar tijd = new GregorianCalendar();
            if (txtSmUur.getText().equals("")) {
                txtSmUur.setText("" + 0);
            }
            if (txtSmMinuten.getText().equals("")) {
                txtSmMinuten.setText("" + 0);
            }
            tijd.set(GregorianCalendar.HOUR_OF_DAY, Integer.parseInt(txtSmUur.getText()));
            tijd.set(GregorianCalendar.MINUTE, Integer.parseInt(txtSmMinuten.getText()));
            samenkomst.setUur(tijd);
            if(chkSmPrincode.isSelected()) {
                samenkomst.setPrincode(1);
            } else {
                samenkomst.setPrincode(0);
            }
        
        
            dao.toevoegenSamenkom(samenkomst);
        
        
        
            vulSamenkomst();
            vulTblSamenkoms();
