    private void initHandmatigSamenkom() {
        int grootste = 0;
        vulSamenkomst();
        for(int i = 0; i < samenkomsten.size(); i++) {
            if (samenkomsten.get(i).getRefnum().getRefnum() > grootste) {
                grootste = samenkomsten.get(i).getRefnum().getRefnum();
            }
        }
        txtRefnum.setText("" + (grootste + 1));
        
        
    }
    
    private void initHandmatigVerantw() {

        String bestandLanden;
        try {
        bestandLanden = "bestanden/landen.txt";
        System.out.println(bestandLanden);
        String[] landen = vulComboLanden(bestandLanden);
        for(int i = 0; i < landen.length; i++) {
            cmbLand.addItem(landen[i]);
        }
        cmbLand.setSelectedIndex(23); 
        vulVerantwoordelijken();
        int grootste = 0;
        for(int i = 0; i < verantwoordelijken.size(); i++) {
            if (verantwoordelijken.get(i).getVref() > grootste) {
                grootste = verantwoordelijken.get(i).getVref();
            }
            
        }
        txtVref.setText("" + (grootste + 1));
        VerantwDao dao = new VerantwDao(drm.getConnection());
        List<String> titels = dao.geefAlleTitels();
        for(int i = 0; i < titels.size(); i++) {
            cmbTitel.addItem(titels.get(i));
           
        }
        } catch(Exception ex) {
            ex.printStackTrace();
        }
        
        
        
        
    }
    
