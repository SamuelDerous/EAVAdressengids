    private void btnToevoegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnToevoegenActionPerformed
        String referentie = "referentienummer";
        try {
        VerantwDao dao = new VerantwDao(drm.getConnection());
        CodesDao codesDao = new CodesDao(drm.getConnection());
        GemorgDao gemorgDao = new GemorgDao(drm.getConnection());
        Verantw verantwoordelijke = dao.getVerantwoordelijkeByVRef(Integer.parseInt(txtVref.getText()));
        List<Codes> codes = new ArrayList<Codes>();
        
        if(verantwoordelijke != null) {
            JOptionPane.showMessageDialog(this,
                "Het referentienummer dient uniek te zijn. Het nummer " + txtVref.getText() + " bestaat al",
                "Fout in de uniciteit van de tabel",
                JOptionPane.ERROR_MESSAGE);
        } else {
            verantwoordelijke = new Verantw();
            verantwoordelijke.setVref(Integer.parseInt(txtVref.getText()));
            verantwoordelijke.setTitel(cmbTitel.getSelectedItem().toString());
            verantwoordelijke.setVoornaam(txtVoornaam.getText());
            verantwoordelijke.setNaam1(txtNaam1.getText());
            verantwoordelijke.setNaam2(txtNaam2.getText());
            verantwoordelijke.setStraat(txtStraat.getText());
            verantwoordelijke.setPostcode(txtPostcode.getText());
            verantwoordelijke.setWoonpl(txtWoonpl.getText());
            verantwoordelijke.setLand(cmbLand.getSelectedItem().toString());
            verantwoordelijke.setTelefoon(txtTelefoon.getText());
            verantwoordelijke.setGsm(txtFaxnum.getText());
            verantwoordelijke.setEmail(txtEmail.getText());
            verantwoordelijke.setTaalcode(txtVerantwTaalcode.getText());
            if(chkPrincode.isSelected()) {
                verantwoordelijke.setPrincode(1);
            } else {
                verantwoordelijke.setPrincode(0);
            }
            boolean volgnumFout = false;
            
            for(int i = 0; i < kerkrijen; i++) {
                if((tblKerken.getValueAt(i, 0) != null) && !(tblKerken.getValueAt(kerkrijen - 1, 0).equals(""))) {
                    for(int j = 1; j < tblKerken.getColumnCount(); j++) {
                        if (tblKerken.getValueAt(i, j) == null) {
                            tblKerken.setValueAt("", i, j);
                        }
                    }
                    Gemorg gemorg = gemorgDao.getGemorgByName(tblKerken.getValueAt(i, 0).toString());
                    // zet ze in een lijst want eerst moet de verantwoordelijke aangemaakt worden in de databank
                    
                    Codes code = new Codes();
                    code.setId((codesDao.getHighest() + 1));
                    code.setVref(verantwoordelijke);
                    code.setRefnum(gemorg);
                    code.setDenomin(gemorg.getDenomin());
                    code.setFunccode(tblKerken.getValueAt(i, 1).toString());
                    code.setFunccod2(tblKerken.getValueAt(i,2).toString());
                    code.setClientnr(tblKerken.getValueAt(i, 3).toString());
                    int volgnummer = 0;
                    if(!tblKerken.getValueAt(i, 4).equals("")) {
                        referentie = "volgnummer";
                        volgnummer = Integer.parseInt(tblKerken.getValueAt(i, 4).toString());
                    }
                    code.setVolgnum(volgnummer);
                    if((Boolean) tblKerken.getValueAt(i, 5)) {
                        code.setPrincode(1);
                    } else {
                        code.setPrincode(0);
                    }
                    
                    List<Codes> lstKerken = codesDao.getVerantwPerKerk(tblKerken.getValueAt(i,0).toString());
                    for(int k = 0; k < lstKerken.size(); k++) {
                        if(code.getVolgnum() == lstKerken.get(k).getVolgnum()) {
                            JOptionPane.showMessageDialog(this, "Er is al een verantwoordelijke met dit volgnummer", "Integriteitsfout", JOptionPane.ERROR_MESSAGE);
                            volgnumFout = true;
                            break;
                        } 
                        
                    }
                    codes.add(code);
           
                }
            }
            if(!volgnumFout) {
            
        
            dao.toevoegenVerantw(verantwoordelijke);
            for(int i = 0; i < codes.size(); i++) {
              codesDao.toevoegenCode(codes.get(i));
            }
            
            
           
            
            
            
            
            
            
            
            txtVerantwVakkenLegen();
        
            vulVerantwoordelijken();
            vulTblVerantwoordelijken();
            vulTblKerk();
            DefaultTableModel model = (DefaultTableModel) tblGegevens.getModel();
            DefaultTableModel model2 = (DefaultTableModel) tblKerken.getModel();
            model.fireTableDataChanged();
            model2.fireTableDataChanged();
        }
        }
        } catch(NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                "Het " + referentie + " dient uitsluitend uit cijfers te bestaan",
                "Is geen nummmer",
                JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_btnToevoegenActionPerformed

